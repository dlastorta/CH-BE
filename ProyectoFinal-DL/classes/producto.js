class Producto {
    constructor(id, timestamp, nombre, descripcion, codigo, thumbnail, precio, stock) {
        this.id = id,
        this.timestamp = timestamp,
        this.nombre = nombre,
        this.descripcion = descripcion,
        this.codigo = codigo,
        this.thumbnail = thumbnail,
        this.precio = precio,
        this.stock = stock
    }
}

export default Producto;